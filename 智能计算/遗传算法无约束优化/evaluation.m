function Y=evaluation(P)
[x1 y1]=size(P);
H=zeros(x1,1);
for i = 1:x1
    A=bi2de(P(i,1:20));
    x=-40+A*(40-(-40))/(2^20-1);
    B=bi2de(P(i,21:40));
    y=-40+B*(40-(-40))/(2^20-1);
    H(i,1)= 0.4/(1+0.02*((x-(-20)).^2 +(y-(-20)).^2))...
    + 0.2/(1+0.5*((x-(-5)).^2 +(y-(-25)).^2))...
    + 0.7/(1+0.01*((x-(0)).^2 +(y-(30)).^2))...
    + 1/(1+2*((x-(30)).^2 +(y-(0)).^2))...
    + 0.05/(1+0.1*((x-(30)).^2 +(y-(-30)).^2));
end
Y=H;